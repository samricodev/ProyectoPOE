    public frmMostrarProducto( Producto product) {
        initComponents();
        
        DefaultTableModel modelo = new DefaultTableModel();
        String cabecera[] = {"Id" , "Nombre" , "Contenido/(ml)" , "Unidades" , "Costo"};
        modelo.setColumnIdentifiers(cabecera);
        producto = product;
        Object arreglo [] = new Object[5];
        
        //MOSTRAR 
        Producto aux = product;
        if(product == null){
            JOptionPane.showMessageDialog(null, "LISTA VACIA");
        }else{
            while(aux != null){
                arreglo[0] = aux.getId();
                arreglo[1] = aux.getNombre();
                arreglo[2] = aux.getContenido();
                arreglo[3] = aux.getUnidades();
                arreglo[4] = aux.getCosto();
                modelo.addRow(arreglo);
                aux = aux.getSiguiente();
            }
            jtTabla.setModel(modelo);
        }
    }


